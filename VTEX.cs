    public void WriteInner(Stream output)
    {
        writer = new BinaryWriter(output);

        // Version
        writer.Write((ushort)1);

        // Flags
        writer.Write((ushort)0);

        // Reflectivity RGBA
        writer.Write(reflectivity.X);
        writer.Write(reflectivity.Y);
        writer.Write(reflectivity.Z);
        writer.Write(0.0f);

        // Width, Height, Depth
        writer.Write(width);
        writer.Write(height);
        writer.Write((ushort)1);

        // Format: PNG_RGBA8888
        writer.Write((byte)16);
        
        // Number of MIP levels
        writer.Write((byte)1);

        // Picmip0Res
        writer.Write((uint)0);

        // ExtraData Offset+Count
        writer.Write((uint)8);
        writer.Write((uint)1);

        writer.Write((uint)0xFFFF);
        writer.Write((uint)8);
        writer.Write((uint)1024);

        writer.Write(new byte[1024]);

        // Texture Data
        writer.Write(imageData);
    }
